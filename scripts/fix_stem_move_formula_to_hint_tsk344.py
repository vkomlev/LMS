import psycopg2
import psycopg2.extras

PROJECT_ROOT = Path(__file__).resolve().parent.parent

_mcp = json.loads((PROJECT_ROOT / ".mcp.json").read_text(encoding="utf-8"))
_dsn = _mcp["mcpServers"]["learn_prod_db"]["args"][-1]
_parsed = urlparse(_dsn)
