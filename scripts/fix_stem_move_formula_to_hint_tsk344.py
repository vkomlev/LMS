HINT_63 = (
    "Цена дана за килограмм, а вес — в граммах: сколько граммов в одном "
    "килограмме, и как перевести вес покупки в килограммы? Дальше стоимость "
    "считается как обычно — цена за килограмм умножить на количество "
    "килограммов. Получится дробное число рублей, например 47.386. Сколько "
    "здесь целых рублей — как получить целую часть числа в Python? "
    "Оставшаяся дробная часть (0.386) переводится в копейки — на что её "
    "нужно умножить, и как округлить результат до целого числа?"
)

TASKS = {
    57: (OLD_STEM_57, NEW_STEM_57, HINT_57),
    58: (OLD_STEM_58, NEW_STEM_58, HINT_58),
    60: (OLD_STEM_60, NEW_STEM_60, HINT_60),
    63: (OLD_STEM_63, NEW_STEM_63, HINT_63),
}


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Убрать формулу из stem, добавить наводящий вопрос в hints_text (tsk-344)"
    )
    parser.add_argument("--apply", action="store_true", help="Записать (по умолчанию dry-run)")
    args = parser.parse_args()

    conn = psycopg2.connect(**PROD)
    conn.autocommit = False
    cur = conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor)

    print("=== tsk-344: формула из stem -> hints_text (57, 58, 60, 63) ===")
    print(f"Подключение: {PROD['user']}@{PROD['host']}/{PROD['dbname']}")
    print(f"Режим: {'APPLY' if args.apply else 'DRY-RUN'}")

    ok_ids: list[int] = []
    for task_id, (old_stem, new_stem, hint) in TASKS.items():
        cur.execute("SELECT task_content FROM tasks WHERE id = %s", (task_id,))
        row = cur.fetchone()
        if row is None:
            print(f"\nid={task_id}: ОТКАЗ — задание не найдено.")
            continue
        content = row["task_content"]
        stem = content.get("stem") or ""
        if stem != old_stem:
            print(f"\nid={task_id}: ОТКАЗ — текущий stem не совпал дословно с ожидаемым "
                  "(мог измениться с момента диагностики). Разобрать вручную.")
            print("--- ТЕКУЩИЙ STEM ---\n" + stem)
            continue
        print(f"\n--- id={task_id} BEFORE ---\n{stem}")
        print(f"--- id={task_id} AFTER ---\n{new_stem}")
        print(f"--- id={task_id} HINT (hints_text[0]) ---\n{hint}")
        ok_ids.append(task_id)

    if not ok_ids:
        print("\nОТКАЗ: ни одно задание не прошло сверку stem. Ничего не сделано.")
        conn.rollback()
        conn.close()
        return 1

    if not args.apply:
        print(f"\nDRY-RUN: изменения НЕ записаны ({len(ok_ids)}/{len(TASKS)} готовы к записи). "
              "Для записи — DBCHECK_OK=1 ... --apply.")
        conn.rollback()
        conn.close()
        return 0

    updated = 0
    for task_id in ok_ids:
        old_stem, new_stem, hint = TASKS[task_id]
        cur.execute("SELECT task_content FROM tasks WHERE id = %s", (task_id,))
        content = cur.fetchone()["task_content"]
        new_content = dict(content)
        new_content["stem"] = new_stem
        new_content["has_hints"] = True
        new_content["hints_text"] = [hint]
        cur.execute(
            "UPDATE tasks SET task_content = %s::jsonb WHERE id = %s",
            (json.dumps(new_content, ensure_ascii=False), task_id),
        )
        if cur.rowcount != 1:
            conn.rollback()
            conn.close()
            print(f"\nROLLBACK: id={task_id} обновил {cur.rowcount} строк вместо 1.")
            return 1
        updated += 1

    # verify в той же транзакции
    cur.execute(
        "SELECT id, task_content FROM tasks WHERE id = ANY(%s)",
        (ok_ids,),
    )
    bad = []
    for r in cur.fetchall():
        old_stem, new_stem, hint = TASKS[r["id"]]
        tc = r["task_content"]
        if tc.get("stem") != new_stem or tc.get("hints_text") != [hint] or tc.get("has_hints") is not True:
            bad.append(r["id"])
    if bad:
        conn.rollback()
        conn.close()
        print(f"\nROLLBACK: верификация не прошла для id={bad}.")
        return 1

    conn.commit()
    print(f"\nCOMMIT: обновлено {updated} заданий ({ok_ids}). Верификация пройдена.")
    conn.close()
    return 0


if __name__ == "__main__":
    try:
        sys.exit(main())
    except Exception as e:  # noqa: BLE001
        print("FAIL:", e, file=sys.stderr)
        sys.exit(1)
