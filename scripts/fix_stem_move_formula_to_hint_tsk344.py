"""Убрать формулу/оператор решения из stem, добавить наводящий вопрос в hints_text
(tsk-344, курс 103 «Числа в Python»).

Проблема (оператор, живой разбор): курс формирует ДВА навыка — (1) алгоритмическое
мышление (перевести словесную задачу в шаги) и (2) записать алгоритм на Python.
Если готовая формула/оператор стоит прямо в `stem` (виден ДО попытки безусловно),
навык 1 не тренируется вообще — ученик транскрибирует данное выражение.
`hints_text` — отдельная LMS-сущность, скрытая по умолчанию, раскрывается по
запросу ученика ПОСЛЕ попытки подумать самому — туда и переносится подсказка.

Правило зафиксировано в methodist/references/assignment-rules.md §3.4a п.3.

4 задания (id=57,58,60,63), курс 103:
- 57: убрана "(используйте оператор `% 10`)" — последняя цифра суммы.
- 58: убраны готовые подвыражения "(`d // 400`)"/"(`d % 400`)" — круги на стадионе.
      Подсказка — дословно формулировка оператора (бегун/900 метров).
- 60: убрана "Используйте формулу `int(X * 10) % 10`." — первая цифра после точки.
- 63: убрана вся формула cost/int/round — рубли и копейки. Задание 63 не имело
      ключей has_hints/hints_text/hints_video вовсе — добавляются.

Демо-числа в подсказках (138, 5.73, 47.386, 900) НЕ пересекаются с зачётными
входами каждого задания (см. assignment-rules §9 п.3 про утечку через пример).

Безопасность (/db-check Режим записи): dry-run по умолчанию; --apply пишет по
одному id в транзакции с точной сверкой текущего stem (защита от дрейфа данных
с момента диагностики) → verify → commit, иначе rollback. Прод-подключение —
из .mcp.json, пароль не печатается.

Запуск (из корня LMS):
  python scripts/fix_stem_move_formula_to_hint_tsk344.py                       # dry-run
  DBCHECK_OK=1 python scripts/fix_stem_move_formula_to_hint_tsk344.py --apply  # запись
"""
