def _dsn() -> str:
    cfg = json.loads(Path(".mcp.json").read_text(encoding="utf-8"))
    args = cfg["mcpServers"]["learn_prod_db"]["args"]
    dsn = next(a for a in args if a.startswith("postgres"))
    return dsn.replace("postgresql+asyncpg://", "postgresql://")


async def main(apply: bool) -> None:
    conn = await asyncpg.connect(_dsn())
    try:
        async with conn.transaction():
            for mid, anchor, ascii_raw in DATA:
                text = await conn.fetchval(
                    "SELECT content->>'text' FROM materials WHERE id=$1", mid)
                if text is None:
                    raise RuntimeError(f"мат {mid}: не найден")
                if "cb-ascii" in text:
                    raise RuntimeError(f"мат {mid}: уже содержит cb-ascii — прерываю")
                cnt = text.count(anchor)
                if cnt != 1:
                    raise RuntimeError(f"мат {mid}: якорь встречается {cnt} раз (нужно 1)")
                block = PRE_OPEN + escape(ascii_raw) + PRE_CLOSE
                new_text = text.replace(anchor, anchor + block)
                await conn.execute(
                    "UPDATE materials SET content = jsonb_set(content,'{text}', to_jsonb($2::text)) WHERE id=$1",
                    mid, new_text)
                check = await conn.fetchval(
                    "SELECT content->>'text' FROM materials WHERE id=$1", mid)
                assert "cb-ascii" in check, "вставка не подтвердилась"
                print(f"OK мат {mid}: +{len(block)} симв. cb-ascii")
            if not apply:
                raise RuntimeError("DRY-RUN: откатываю (запусти с --apply)")
        print("\nЗАПИСАНО И ЗАКОММИЧЕНО.")
    finally:
        await conn.close()


if __name__ == "__main__":
    apply = "--apply" in sys.argv
    try:
        asyncio.run(main(apply))
    except RuntimeError as exc:
        print(f"\n{exc}")
        sys.exit(0 if "DRY-RUN" in str(exc) else 1)
