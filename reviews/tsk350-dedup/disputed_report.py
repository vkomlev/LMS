INVIS = dict.fromkeys([0x00AD, 0x200B, 0x200C, 0x200D, 0x2060, 0xFEFF], None)


def plain(row: dict, limit: int = 320) -> str:
    """Человекочитаемое условие: без HTML, без мягких переносов, одной строкой."""
    tc = row["task_content"] or {}
    s = strip_html(tc.get("stem") or "")
    s = s.translate(INVIS)
    s = unicodedata.normalize("NFKC", s)
    s = re.sub(r"\s+", " ", html.unescape(s)).strip()
    return s[:limit] + ("…" if len(s) > limit else "")


def theme_num(row: dict) -> str:
    tc = row["task_content"] or {}
    n = tc.get("task_number_ege")
    if n:
        return f"Задание {n} ЕГЭ"
    # из названия курса: «…Задание №3…», «Задание 2 ЕГЭ…», «Задание 19-21…»
    m = re.search(r"[Зз]адани[еяй]\s*№?\s*([\d\-–]+)", row.get("course_title") or "")
    return f"Задание {m.group(1)} ЕГЭ" if m else f"курс {row['course_id']}"


def block(r: dict) -> str:
    lbl, src = source_link(r)
    o = f"- **[задание в LMS]({lms_url(r)})**"
    if src:
        o += f" · первоисточник: [{lbl}]({src})"
    else:
        o += f" · источник: {lbl}"
    o += f"\n  > {plain(r)}\n"
    return o


def main() -> None:
    gs, weak = find_groups()
    by_reason: dict[str, list] = defaultdict(list)
    for w in weak:
        by_reason[w["reason"]].append(w)

    o = io.StringIO()
    o.write("# tsk-350 — спорные пары заданий ЕГЭ (не тронуты)\n\n")
