"""Валидатор реального ФИО (tsk-223, Фаза A).

Единый серверный источник правды по формату ФИО ученика в поле
`users.full_name`. Формат — «Фамилия Имя [Отчество]» русскими буквами.

Функция :func:`validate_full_name` нормализует и проверяет строку, бросая
понятный русский :class:`ValueError` при нарушении. Для переиспользования из
Pydantic-схем экспортируется аннотированный тип :data:`FullNameStr`
(``Annotated[str, AfterValidator(validate_full_name)]``) — тот же механизм
проверки без дублирования правил.
"""
import logging
import re
from typing import Annotated

from pydantic import AfterValidator

logger = logging.getLogger(__name__)

# Итоговая длина нормализованной строки.
MIN_LENGTH = 3
MAX_LENGTH = 100

# Один токен ФИО: только кириллические буквы, допускается внутренний дефис
# (двойные фамилии «Мамин-Сибиряк»). Каждый сегмент вокруг дефиса — минимум
# одна буква, поэтому первый символ токена всегда буква, а двойной дефис и
# дефис на краю невозможны.
_TOKEN_RE = re.compile(r"^[А-Яа-яЁё]+(?:-[А-Яа-яЁё]+)*$")


def validate_full_name(value: str) -> str:
    """Нормализовать и проверить ФИО, вернуть каноническую строку.

    Нормализация: обрезаются краевые пробелы, повторные пробелы схлопываются
    в один.

    Правила формата «Фамилия Имя [Отчество]»:
    - итоговая длина 3..100 символов;
    - не менее 2 токенов через пробел (фамилия + имя; отчество — 3-й, опц.);
    - каждый токен — только кириллические буквы (А-Яа-яЁё), допускается
      внутренний дефис; первый символ токена — буква;
    - запрещены латиница, цифры, знаки препинания (кроме внутреннего дефиса),
      emoji и прочие спецсимволы.

    Args:
        value: сырое значение ФИО из запроса.

    Returns:
        Нормализованная строка «Фамилия Имя [Отчество]».

    Raises:
        ValueError: если значение не соответствует формату; текст сообщения —
            на русском и указывает нарушенное правило.
    """
    if value is None:  # type: ignore[redundant-expr] — защита от рантайм-None
        raise ValueError("ФИО не может быть пустым.")

    # Нормализация: strip + схлопывание повторных пробелов (любых пробельных
    # символов) в один. split()/join делает это за один проход.
    normalized = " ".join(value.split())

    if not normalized:
        raise ValueError("ФИО не может быть пустым.")

    if len(normalized) < MIN_LENGTH:
        raise ValueError(
            f"ФИО слишком короткое: минимум {MIN_LENGTH} символа."
        )
    if len(normalized) > MAX_LENGTH:
        raise ValueError(
            f"ФИО слишком длинное: максимум {MAX_LENGTH} символов."
        )

    tokens = normalized.split(" ")
    if len(tokens) < 2:
        raise ValueError(
            "Укажите минимум фамилию и имя — два слова через пробел."
        )

    for token in tokens:
        if not _TOKEN_RE.match(token):
            raise ValueError(
                "Каждое слово ФИО должно состоять только из русских букв "
                f"(допускается дефис внутри): «{token}»."
            )

    return normalized


# Переиспользуемый Pydantic-тип: та же проверка через AfterValidator, без
# дублирования правил. Использовать в схемах, где нужна автоматическая
# 422-валидация (например `full_name: FullNameStr`).
FullNameStr = Annotated[str, AfterValidator(validate_full_name)]
